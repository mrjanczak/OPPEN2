     * more tables.
     *
     * @param array $row The row returned by PDOStatement->fetch(PDO::FETCH_NUM)
     * @param int $startcol 0-based offset column which indicates which resultset column to start with.
     * @param boolean $rehydrate Whether this object is being re-hydrated from the database.
     * @return int             next starting column
     * @throws PropelException - Any caught Exception will be rewrapped as a PropelException.
     */
    public function hydrate($row, $startcol = 0, $rehydrate = false)
    {
        try {

            $this->id = ($row[$startcol + 0] !== null) ? (int) $row[$startcol + 0] : null;
            $this->no = ($row[$startcol + 1] !== null) ? (string) $row[$startcol + 1] : null;
            $this->name = ($row[$startcol + 2] !== null) ? (string) $row[$startcol + 2] : null;
            $this->symbol = ($row[$startcol + 3] !== null) ? (string) $row[$startcol + 3] : null;
            $this->formula = ($row[$startcol + 4] !== null) ? (string) $row[$startcol + 4] : null;
            $this->report_id = ($row[$startcol + 5] !== null) ? (int) $row[$startcol + 5] : null;
            $this->tree_left = ($row[$startcol + 6] !== null) ? (int) $row[$startcol + 6] : null;
            $this->tree_right = ($row[$startcol + 7] !== null) ? (int) $row[$startcol + 7] : null;
            $this->tree_level = ($row[$startcol + 8] !== null) ? (int) $row[$startcol + 8] : null;
            $this->resetModified();

            $this->setNew(false);

            if ($rehydrate) {
                $this->ensureConsistency();
            }
            $this->postHydrate($row, $startcol, $rehydrate);

            return $startcol + 9; // 9 = ReportEntryPeer::NUM_HYDRATE_COLUMNS.

        } catch (Exception $e) {
            throw new PropelException("Error populating ReportEntry object", $e);
        }
    }

