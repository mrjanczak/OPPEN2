            $this->send_reminder = ($row[$startcol + 5] !== null) ? (boolean) $row[$startcol + 5] : null;
            $this->user_id = ($row[$startcol + 6] !== null) ? (int) $row[$startcol + 6] : null;
            $this->project_id = ($row[$startcol + 7] !== null) ? (int) $row[$startcol + 7] : null;
            $this->sortable_rank = ($row[$startcol + 8] !== null) ? (int) $row[$startcol + 8] : null;
